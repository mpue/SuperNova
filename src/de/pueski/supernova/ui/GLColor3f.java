package de.pueski.supernova.ui;

public class GLColor3f {

	float red;
	float green;
	float blue;
	
	public GLColor3f(float red, float green, float blue) {
		super();
		this.red = red;
		this.green = green;
		this.blue = blue;
	}

	/**
	 * @return the red
	 */
	public float getRed() {
		return red;
	}

	/**
	 * @param red the red to set
	 */
	public void setRed(float red) {
		this.red = red;
	}

	/**
	 * @return the green
	 */
	public float getGreen() {
		return green;
	}

	/**
	 * @param green the green to set
	 */
	public void setGreen(float green) {
		this.green = green;
	}

	/**
	 * @return the blue
	 */
	public float getBlue() {
		return blue;
	}

	/**
	 * @param blue the blue to set
	 */
	public void setBlue(float blue) {
		this.blue = blue;
	}

	
	
}
